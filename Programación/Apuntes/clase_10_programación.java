class Main {
    public static void main(String[] args){
        int n = 10;
        System.out.print( suma(5));
    }
    static int suma(int n){
        int s = 0;
        int i = 1;
        while(i <= n){
            s = s + i;
            i++;
        }
        return s;
    } 
}
